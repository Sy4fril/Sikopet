    public notif() {
        initComponents();
        // Ambil id warga dari parent (WargaPage)
        Integer idWarga = getIdWargaFromParent();
        if (idWarga != null) {
            loadNotifikasi(idWarga);
        } else {
            notifikasi.setText("Tidak dapat mengambil notifikasi. ID warga tidak ditemukan.");
        }
    }

    // Mengambil id warga dari parent (WargaPage)
    private Integer getIdWargaFromParent() {
        java.awt.Container parent = getParent();
        while (parent != null && !(parent instanceof WargaPage)) {
            parent = parent.getParent();
        }
        if (parent instanceof WargaPage) {
            WargaPage wp = (WargaPage) parent;
            if (wp.getWarga() != null) {
                return wp.getWarga().getId();
            }
        }
        return null;
    }

    // Mengambil dan menampilkan daftar notifikasi untuk id warga
    public void loadNotifikasi(int idWarga) {
        System.out.println("[DEBUG] loadNotifikasi dipanggil untuk idWarga=" + idWarga);
        List<String> list = NotifikasiDAO.getNotifikasiByIdWarga(idWarga);
        System.out.println("[DEBUG] Jumlah notifikasi ditemukan: " + list.size());
        StringBuilder sb = new StringBuilder();
        if (list.isEmpty()) {
            sb.append("Belum ada notifikasi untuk Anda. üòä");
        } else {
            for (String s : list) {
                String lower = s.toLowerCase();
                String emoji = "";
                if (lower.contains("penting") || lower.contains("perhatian") || lower.contains("segera")) {
                    emoji = "‚ö†Ô∏è ";
                } else if (lower.contains("ulang tahun") || lower.contains("selamat")) {
                    emoji = "üéâ ";
                } else if (lower.contains("broadcast") || lower.contains("semua warga") || lower.contains("ketua")) {
                    emoji = "üì¢ ";
                } else if (lower.contains("personal") || lower.contains("khusus") || lower.contains("anda")) {
                    emoji = "üë§ ";
                } else if (lower.contains("baru") || lower.contains("pesan")) {
                    emoji = "üì® ";
                } else {
                    emoji = "üîî ";
                }
                sb.append(emoji).append(s).append("\n\n");
            }
        }
        notifikasi.setText(sb.toString());
    }

